                        <h2 class="card-title text-center mb-4">Connexion</h2>
                        
                        <?php if ($error): ?>
                            <div class="alert alert-danger"><?php echo htmlspecialchars($error); ?></div>
                        <?php endif; ?>

                        <form method="POST" action="/login">
                            <div class="mb-3">
